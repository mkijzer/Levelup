const CACHE_NAME = "levelup-v1";
const STATIC_ASSETS = [
  "/",
  "/css/style.css",
  "/js/main.js",
  "/data/articles.json",
];

self.addEventListener("install", (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => cache.addAll(STATIC_ASSETS))
  );
});

self.addEventListener("fetch", (event) => {
  event.respondWith(
    caches
      .match(event.request)
      .then((response) => response || fetch(event.request))
  );
});
